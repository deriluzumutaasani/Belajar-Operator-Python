# Operator relasional : < > <= >= ==(sama dengan) !=(bukan sama dengan)
b_1 = 3
b_2 = 7

print(f"{b_1} < {b_2} = {b_1 < b_2}")
print(f"{b_1} > {b_2} = {b_1 > b_2}")
print(f"{b_1} <= {b_2} = {b_1 <= b_2}")
print(f"{b_1} >= {b_2} = {b_1 >= b_2}")
print(f"{b_1} == {b_2} = {b_1 == b_2}")
print(f"{b_1} != {b_2} = {b_1 != b_2}")