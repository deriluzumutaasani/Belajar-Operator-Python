# Operator aritmatika : perhitungan matematis dasar

# operator tambah pada tipe bilangan (inteljen / float)
b_1 = 7
b_2 = 3
hsl_tbh = b_1 + b_2
print(f"{b_1} + {b_2} = {hsl_tbh}")

# operator tambah pada tipe string
t_1 = "Hello "
t_2 = "World"
hsl_tbh_t = t_1 + t_2
print(f"{t_1} + {t_2} = {hsl_tbh_t}")

# operator kurang
hsl_krg = b_1 - b_2
print(f"{b_1} - {b_2} = {hsl_krg}")

# operator kali
hsl_kl = b_1 * b_2
print(f"{b_1} x {b_2} = {hsl_kl}")

# operator pangkat
hsl_pkt = b_1 ** b_2
print(f"{b_1} ^ {b_2} = {hsl_pkt}")

# operator bagi
hsl_bg = b_1 / b_2
print(f"{b_1} / {b_2} = {hsl_bg}")

# operator floor (//) - hasil bagi yg dibulatkan
hsl_bgf = b_1 // b_2
print(f"{b_1} // {b_2} = {hsl_bgf}")

# operator modulus (%) - sisa hasil bagi
hsl_mod = b_1 % b_2
print(f"{b_1} % {b_2} = {hsl_mod}")

